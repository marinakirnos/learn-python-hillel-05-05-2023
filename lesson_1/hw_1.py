# Task 1. Написати програму, яка конвертує градуси у радіани (або навпаки, за бажанням, але вкажіть що саме обрали!). 180 градусів - це Pi радіан.
# Взідні дані:
# градуси (або радіани).
# На виході:
# конвертована величина (градуси або радіани) округлена до 5 цифр після коми

import math
x = float(input('Введите радианы x = '))
y = round((x * 180 / math.pi ), 5)
print(y)

